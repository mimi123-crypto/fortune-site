def build_deck():
    """78枚のフルデッキを dict のリストで返す。"""
    deck = []
    # 大アルカナ
    for name, up, rev in MAJOR_ARCANA:
        deck.append({
            "arcana": "major",
            "name": name,
            "suit": None,
            "theme": None,
            "upright": up,
            "reverse": rev,
        })
    # 小アルカナ（スート×ランク = 4×14 = 56枚）
    for suit_name, theme in SUITS:
        for rank_disp, up, rev in RANKS:
            deck.append({
                "arcana": "minor",
                "name": f"{suit_name}の{rank_disp}",
                "suit": suit_name,
                "theme": theme,
                "upright": up,
                "reverse": rev,
            })
    return deck


