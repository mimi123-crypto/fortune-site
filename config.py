def make_rakuten_link(keyword: str) -> str:
    """商品キーワードから楽天リンクを生成。アフィリIDがあればアフィリURL、無ければ通常検索URL。"""
    import urllib.parse
    q = urllib.parse.quote(keyword)
    search_url = f"https://search.rakuten.co.jp/search/mall/{q}/"
    if RAKUTEN_AFFILIATE_ID:
        # 楽天アフィリエイトのリンク形式（登録後に正式な形式へ要調整）
        enc = urllib.parse.quote(search_url, safe="")
        return f"https://hb.afl.rakuten.co.jp/hgc/{RAKUTEN_AFFILIATE_ID}/?pc={enc}"
    return search_url
